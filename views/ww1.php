                <h1 id="ww1">Primera Guerra Mundial</h1>
                <p>La Primera Guerra Mundial, anteriormente llamada la Gran Guerra,
                fue una confrontación bélica centrada en Europa que empezó el 28 de 
                julio de 1914 y finalizó el 11 de noviembre de 1918. Recibió el 
                calificativo de «mundial» porque se vieron involucradas todas las 
                grandes potencias industriales y militares de la época, divididas 
                en dos alianzas. Por un lado, la Triple Alianza formada por las 
                Potencias Centrales: el Imperio alemán y Austria-Hungría. Por otro 
                lado se encontraba la Triple Entente, formada por el Reino Unido, 
                Francia y el Imperio ruso.<br><br>

                Dejó más de ocho millones de víctimas militares y 6,6 millones de 
                víctimas civiles. Murieron casi el 60 por ciento de las personas 
                que lucharon. Muchas más desaparecieron o resultaron heridas. En 
                solo cuatro años, entre 1914 y 1918, la Primera Guerra Mundial cambió 
                los conflictos bélicos modernos, convirtiéndose en uno de los más 
                letales en la historia mundial... Pero ¿cuales fueron las causas de 
                este conflicto?</p><br>

                <h2 id="causas">Las causas de la guerra</h2>
                <p>La Primera Guerra Mundial tuvo diversas causas, pero sus raíces se 
                encuentran en una compleja red de alianzas entre las potencias europeas. 
                En esencia, fue la desconfianza entre la informal Triple Entente y 
                la secreta Triple Alianza. Los actores más poderosos, Gran Bretaña, 
                Rusia y Alemania, gobernaban imperios coloniales mundiales que querían 
                expandir y proteger. A lo largo del siglo XIX, consolidaron su poder y 
                se protegieron forjando alianzas con otras potencias europeas.<br><br>
                
                En julio de 1914, las tensiones entre la Triple Entente (también conocida 
                como los Aliados) y la Triple Alianza (también denominada Potencias Centrales) 
                escalaron tras el asesinato del archiduque Francisco Fernando, heredero al 
                trono de Austria-Hungría, por parte de un nacionalista serbio-bosnio durante 
                su visita a Sarajevo. Austria-Hungría culpó a Serbia por el ataque. Rusia 
                respaldó a su aliado, Serbia. Cuando Austria-Hungría declaró la guerra a 
                Serbia un mes después, sus aliados intervinieron y el continente entró en 
                guerra.<br></p><br>

                <h2 id="expansion">La expansión de la guerra</h2>
                <p>El conflicto pronto se expandió al resto del mundo y afectó a las colonias 
                y a los países aliados de África, Asia, Oriente Medio y Australia. En 1917, 
                los Estados Unidos entraron en la guerra tras un largo periodo de no intervención. 
                Entonces, el escenario principal de la guerra —el Frente Occidental en Luxemburgo, 
                Países Bajos, Bélgica y Francia— fue el emplazamiento de un bloqueo letal.<br><br>
                
                A pesar de el uso de avances tecnológicos como el gas tóxico o los tanques blindados, 
                ambas facciones estaban atrapadas en una guerra de trincheras que se cobró un gran 
                número de víctimas. Batallas como la de Verdún y la primera batalla del Somme fueron 
                unas de las más mortíferas en la historia del conflicto humano.<br><br>
                
                Con la ayuda de Estados Unidos, los aliados se abrieron paso con la Ofensiva de los 
                Cien Días, que provocó la derrota militar de Alemania. Oficialmente, la guerra llegó 
                a su fin a las 11:11 de la mañana del 11 de noviembre de 1918.<br><br>
                
                Para entonces, el mundo estaba en manos de una pandemia de gripe que afectaría a un 
                tercio de la población mundial. Se habían desatado revoluciones en Alemania, Rusia y 
                otros países. Gran parte de Europa estaba en ruinas. La «neurosis de guerra» y las 
                secuelas de la intoxicación por gas se cobrarían miles de vidas más.<br></p><br>

                <h2 id="nm">¿Nunca más?</h2>
                <p>Aunque el mundo se comprometió a no permitir que ocurriera otra guerra como esa, 
                se sembraron las semillas del siguiente conflicto con el Tratado de Versalles, que 
                fue humillante y punitivo para los alemanes y contribuyó a preparar el terreno para 
                el auge del fascismo y la Segunda Guerra Mundial. La tecnología que había generado 
                la guerra se emplearía en la siguiente guerra mundial solo dos décadas después. Aunque 
                entonces se describió como «la guerra para poner fin a todas las guerras», las cicatrices 
                que dejó la Primera Guerra Mundial en el mundo no acabaron de curar.<br></p><br>
