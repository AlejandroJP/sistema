                <h1 id="intro">Consecuencias de la rotación y traslación de La Tierra</h1>
                <p>La Primera Guerra Mundial, anteriormente llamada la Gran Guerra,
                fue una confrontación bélica centrada en Europa que empezó el 28 de 
                julio de 1914 y finalizó el 11 de noviembre de 1918. Recibió el 
                calificativo de «mundial» porque se vieron involucradas todas las 
                grandes potencias industriales y militares de la época, divididas 
                en dos alianzas. Por un lado, la Triple Alianza formada por las 
                Potencias Centrales: el Imperio alemán y Austria-Hungría. Por otro 
                lado se encontraba la Triple Entente, formada por el Reino Unido, 
                Francia y el Imperio ruso.<br><br>

                El movimiento que efectúa alrededor del Sol determina el paso de los 
                años y, junto con la inclinación del eje terrestre, determina el 
                cambio de estaciones. Aunado a esto, la rotación de la Tierra alrededor 
                de su propio eje, provoca el día y la noche. Veamos esto con más detalle 
                a continuación.</p><br>

                <h2 id="rotacion">El movimiento de traslación</h2>
                <p>Por el movimiento de traslación la Tierra se mueve alrededor del Sol, 
                impulsada por la gravitación, en 365 días, 6 horas y 9 minutos, equivalente 
                a 365,2422 días, que es la duración del año. El pequeño excedente de 0,2422 
                es el motivo por el cual cada cuatro años es bisiesto ya que, durante ese 
                tiempo, suman un día más al año (366) que se refleja el 29 de enero de cada 
                año bisiesto.<br><br>
                
                Nuestro planeta describe una trayectoria elíptica, y está ubicada a una 
                distancia Sol-Tierra media de 1 U.A. (Unidad Astronómica), que equivale a 
                149.675.000 km. En este movimiento, el planeta viaja a una velocidad de 29,5 
                kilómetros por segundo, recorriendo en una hora 106.000 kilómetros, o 
                2.544.000 kilómetros al día.<br><br>

                La excentricidad de la órbita terrestre hace variar la distancia entre la 
                Tierra y el Sol en el transcurso de un año. A principios de enero la Tierra 
                alcanza su máxima proximidad al Sol (perihelio). En julio llega a su máxima 
                lejanía (afelio).<br><br>

                La inclinación de 23,5° del eje de rotación terrestre también propicia la 
                sucesión de las estaciones (verano, otoño, invierno y primavera). Siempre 
                son complementarios (opuestos) en los dos hemisferios de la Tierra. Así, por 
                ejemplo, cuando en el hemisferio norte es invierno, en el hemisferio sur es 
                verano, y viceversa.<br><br>

                Estos contrastes no se deben a que la Tierra esté más o menos alejada del Sol, 
                sino que a lo largo del año la traslación de nuestro planeta provoca que los 
                rayos solares lleguen a cada hemisferio con distinta inclinación.<br></p><br>
